public class Rent {

    private String title, name, phNum;

    public Rent(String title, String name, String phNum) {
        this.title = title;
        this.name = name;
        this.phNum = phNum;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhNum() {
        return phNum;
    }

    public void setPhNum(String phNum) {
        this.phNum = phNum;
    }

}
